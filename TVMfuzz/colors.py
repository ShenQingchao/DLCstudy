

def Red(string):
    return '\033[1;31m' + string + '\033[0m'

def Green(string):
    return '\033[1;32m' + string + '\033[0m'

def Yellow(string):
    return '\033[1;33m' + string + '\033[0m'

def Blue(string):
    return '\033[1;34m' + string + '\033[0m'

def Magenta(string):
    return '\033[1;35m' + string + '\033[0m'

def Cyan(string):
    return '\033[1;36m' + string + '\033[0m'